#Посчитать и вывести количество элементов арифметической прогрессии, удовлетворяющих условию 10<ai<30.
a1 = int(input("Введите первый элемент прогрессии: "))
d = int(input("Введите разность прогрессии: "))
count = 0
n = 1
current = a1
print("Элементы прогрессии, удовлетворяющие условию 10 < ai < 30:")
while True:
    if 10 < current < 30:
        print(f"a{n} = {current}")
        count += 1
    if (d > 0 and current > 30) or (d < 0 and current < 10):
        break
    n += 1
print(f"\nКоличество элементов, удовлетворяющих условию: {count}")
